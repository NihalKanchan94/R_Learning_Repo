#Arithmetic Operators
x <- 10
y <- 3

#Addition
res1 <- x + y
print(res1)

#Subtraction
res2 <- x - y
print(res2)

#Multiplication
res3 <- x * y
print(res3)

#Division
res4 <- x / y
print(res4)

#Exponentiation
res5 <- x ^ y 
print(res5)

res6 <- x ** y
print(res6)

#Modulus
res7 <- x %% y
print(res7)

#Floor Division / Integer Division
res8 <- x %/% y 
print(res8)
